public class ComandoRepeticao {
	
	public static void main(String[] args) {

		int contador = 1;

		while (contador <= 10) {
			System.out.println(contador + "JHPortugal");

			contador++;
		}
		
		for (int i = 1; i <= 10; i++) {
			System.out.println(i + " - for JHPortugal");
		}
		System.out.println("Fim da execução");	
		}
}
